public class Entry
{
    private String name;
    private int number;
    
    public String getName()
    {
        return name;
    }
    
    public void setName(String theName)
    {
        name = theName;
    }
    
    public int getNumber()
    {
        return number;
    }
    
    public void setNumber(int theNumber)
    {
        number = theNumber;
    }
}
